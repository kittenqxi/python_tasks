# Пример:

# matrix = [
#     [1, 2, 3],
#     [4, 5, 6],
#     [7, 8, 9]
# ]
# Что нужно сделать

# Найти сумму элементов главной диагонали.

# Подсказка

# У элементов главной диагонали:

